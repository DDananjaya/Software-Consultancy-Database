--FUNCTION: Calculate Digital Asset Discount
CREATE OR REPLACE FUNCTION CalculateAssetDiscount(
    p_Quantity NUMBER,
    p_AssetID NUMBER
) RETURN NUMBER AS
    v_Discount NUMBER := 0;
    v_Price NUMBER;
BEGIN
    BEGIN
        SELECT Price INTO v_Price FROM DigitalAsset WHERE AssetID = p_AssetID;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            RAISE_APPLICATION_ERROR(-20001, 'Asset not found.');
    END;

    v_Discount := CASE
                    WHEN p_Quantity >= 10 THEN 0.20 * v_Price 
                    WHEN p_Quantity >= 5 THEN 0.10 * v_Price   
                    ELSE 0                                     
                  END;

    RETURN v_Discount;
END CalculateAssetDiscount;
/
DECLARE
    v_DiscAmount NUMBER;
    v_Qty NUMBER := 10;
    v_AssetID NUMBER := 2;
BEGIN
    v_DiscAmount := CalculateAssetDiscount(v_Qty, v_AssetID);
    DBMS_OUTPUT.PUT_LINE('Discount per item for buying ' || v_Qty || ' units: Rs.' || v_DiscAmount);
END;
