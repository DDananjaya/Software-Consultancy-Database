-- 2. PROCEDURE: Read Customer Details
CREATE OR REPLACE PROCEDURE ReadCustomer(
    p_CustomerID NUMBER
) AS
    v_FirstName customer.FirstName%TYPE;
    v_LastName customer.LastName%TYPE;
    v_PhoneNumber customer.PhoneNumber%TYPE;
    v_Email customer.Email%TYPE;
    v_Address customer.Address%TYPE;
BEGIN
    SELECT FirstName, LastName, PhoneNumber, Email, Address
    INTO v_FirstName, v_LastName, v_PhoneNumber, v_Email, v_Address
    FROM customer
    WHERE CustomerID = p_CustomerID
    AND ROWNUM = 1; 

    DBMS_OUTPUT.PUT_LINE('--- Client Profile ---');
    DBMS_OUTPUT.PUT_LINE('Name: ' || v_FirstName || ' ' || v_LastName);
    DBMS_OUTPUT.PUT_LINE('Phone: ' || v_PhoneNumber);
    DBMS_OUTPUT.PUT_LINE('Email: ' || v_Email);
    DBMS_OUTPUT.PUT_LINE('Address: ' || v_Address);
    
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('Client not found with ID: ' || p_CustomerID);
END ReadCustomer;
/
BEGIN
    ReadCustomer(1);  
END;
