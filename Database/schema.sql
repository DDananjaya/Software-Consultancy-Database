--REPORT: Customer Consultation Report
CREATE OR REPLACE PROCEDURE ClientActivityReport AS
    CURSOR client_cur IS
        SELECT c.CustomerID, c.FirstName, c.LastName, 
               COUNT(con.ConsultationID) AS TotalCons,
               SUM(CASE WHEN con.Status = 'Completed' THEN 1 ELSE 0 END) AS Completed
        FROM customer c
        LEFT JOIN Consultation con ON c.CustomerID = con.CustomerID
        GROUP BY c.CustomerID, c.FirstName, c.LastName
        ORDER BY TotalCons DESC;
BEGIN
    DBMS_OUTPUT.PUT_LINE('ID | Name             | Total Meetings | Completed');
    DBMS_OUTPUT.PUT_LINE('--------------------------------------------------');
    FOR r IN client_cur LOOP
        DBMS_OUTPUT.PUT_LINE(r.CustomerID || '  | ' || r.FirstName || ' ' || r.LastName || ' | ' || r.TotalCons || '              | ' || r.Completed);
    END LOOP;
END ClientActivityReport;
/
BEGIN
    DBMS_OUTPUT.PUT_LINE('--- Executing Client Activity Report ---');
    ClientActivityReport;
END;
/

--REPORT: Digital Asset Stock Report
CREATE OR REPLACE PROCEDURE DigitalAssetReport AS
    CURSOR asset_cur IS
        SELECT AssetName, Provider, QuantityAvailable
        FROM DigitalAsset
        ORDER BY QuantityAvailable ASC;
BEGIN
    DBMS_OUTPUT.PUT_LINE('Asset Name             | Provider      | Available Qty');
    DBMS_OUTPUT.PUT_LINE('----------------------------------------------------');
    FOR r IN asset_cur LOOP
        DBMS_OUTPUT.PUT_LINE(RPAD(r.AssetName, 22) || ' | ' || RPAD(r.Provider, 13) || ' | ' || r.QuantityAvailable);
    END LOOP;
END DigitalAssetReport;
/
BEGIN
    DBMS_OUTPUT.PUT_LINE('--- Executing Digital Asset Report ---');
    DigitalAssetReport;
END;
/

--REPORT: Developer Performance Report
CREATE OR REPLACE PROCEDURE DeveloperPerformanceReport AS
    CURSOR dev_cur IS
        SELECT e.EmpID, e.FirstName, e.LastName, e.JobRole,
               NVL(SUM(pt.HoursWorked), 0) AS TotalHours
        FROM Employee e
        LEFT JOIN ProjectTask pt ON e.EmpID = pt.EmpID
        GROUP BY e.EmpID, e.FirstName, e.LastName, e.JobRole
        ORDER BY TotalHours DESC;
BEGIN
    DBMS_OUTPUT.PUT_LINE('Name               | Role               | Hours Worked');
    DBMS_OUTPUT.PUT_LINE('----------------------------------------------------');
    FOR r IN dev_cur LOOP
        DBMS_OUTPUT.PUT_LINE(RPAD(r.FirstName || ' ' || r.LastName, 18) || ' | ' || RPAD(r.JobRole, 18) || ' | ' || r.TotalHours);
    END LOOP;
END DeveloperPerformanceReport;
/
BEGIN
    DBMS_OUTPUT.PUT_LINE('--- Executing Developer Performance Report ---');
    DeveloperPerformanceReport;
END;
/

