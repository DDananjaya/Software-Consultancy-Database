--FUNCTION: Get Consultations For A Project
CREATE OR REPLACE FUNCTION GetProjectConsultations(p_ProjectID NUMBER) 
RETURN SYS_REFCURSOR AS
    c_consultations SYS_REFCURSOR;
BEGIN
    OPEN c_consultations FOR 
        SELECT c.ConsultationID, c.ConsultationDate, c.Topic, c.Status, 
               e.FirstName || ' ' || e.LastName AS EmployeeName
        FROM Consultation c
        JOIN Employee e ON c.EmpID = e.EmpID
        WHERE c.ProjectID = p_ProjectID
        ORDER BY c.ConsultationDate DESC;
    
    RETURN c_consultations;
END GetProjectConsultations;
/
DECLARE
    v_cursor SYS_REFCURSOR;
    v_ID NUMBER;
    v_Date DATE;
    v_Topic VARCHAR2(100);
    v_Status VARCHAR2(20);
    v_EmpName VARCHAR2(200);
BEGIN
    v_cursor := GetProjectConsultations(1);
    
    DBMS_OUTPUT.PUT_LINE('--- Project Consultations ---');
    LOOP
        FETCH v_cursor INTO v_ID, v_Date, v_Topic, v_Status, v_EmpName;
        EXIT WHEN v_cursor%NOTFOUND;
        DBMS_OUTPUT.PUT_LINE(v_ID || ' | ' || v_Date || ' | ' || v_Topic || ' | Handled By: ' || v_EmpName);
    END LOOP;
    CLOSE v_cursor;
END;
/

